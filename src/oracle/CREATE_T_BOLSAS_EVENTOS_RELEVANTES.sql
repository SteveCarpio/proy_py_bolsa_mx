CREATE TABLE T_BOLSAS_EVENTOS_RELEVANTES (
    N NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CLAVE      VARCHAR2(15),
    SECCION    VARCHAR2(80),
    FECHA      DATE,
    ASUNTO     VARCHAR2(500),
    URL        VARCHAR2(350),
    ARCHIVO    VARCHAR2(250),
    ORIGEN     VARCHAR2(6),
    T          VARCHAR2(2),
    FILTRO     VARCHAR2(10)
);


CREATE UNIQUE INDEX idx_T_BOLSAS_EVENTOS_RELEVANTES
ON T_BOLSAS_EVENTOS_RELEVANTES(CLAVE, SECCION, FECHA, ASUNTO, URL, ARCHIVO, ORIGEN, T, FILTRO);